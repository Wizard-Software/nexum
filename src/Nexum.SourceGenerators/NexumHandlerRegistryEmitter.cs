namespace Nexum.SourceGenerators
{
    internal static class NexumHandlerRegistryEmitter
    {
        public static string Emit(string rootNamespace, EquatableArray<HandlerRegistration> registrations)
        {
            var sb = new StringBuilder();
            sb.AppendLine("// <auto-generated/>");
            sb.AppendLine("#nullable enable");
            sb.AppendLine();
            sb.AppendLine($"namespace {rootNamespace}");
            sb.AppendLine("{");
