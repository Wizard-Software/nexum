        private static string MapToServiceLifetime(string? lifetime)
        {
            // Default is Scoped for handlers
            return lifetime is null
                ? "Scoped"
                : lifetime switch
                {
                    "Transient" => "Transient",
                    "Scoped" => "Scoped",
                    "Singleton" => "Singleton",
                    _ => "Scoped"
                };
        }

        private static string MapToNexumLifetime(string? lifetime)
        {
            return lifetime is null
                ? "Scoped"
                : lifetime switch
                {
                    "Transient" => "Transient",
                    "Scoped" => "Scoped",
                    "Singleton" => "Singleton",
                    _ => "Scoped"
                };
        }
    }
