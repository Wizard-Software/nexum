            sb.AppendLine("    [global::System.CodeDom.Compiler.GeneratedCodeAttribute(\"Nexum.SourceGenerators\", \"1.0.0\")]");
            sb.AppendLine("    internal static class NexumHandlerRegistry");
            sb.AppendLine("    {");

            // AddNexumHandlers method
            sb.AppendLine("        /// <summary>");
            sb.AppendLine("        /// Registers all discovered Nexum handlers with the service collection.");
            sb.AppendLine("        /// </summary>");
            sb.AppendLine("        public static void AddNexumHandlers(global::Microsoft.Extensions.DependencyInjection.IServiceCollection services)");
            sb.AppendLine("        {");
            foreach (HandlerRegistration reg in registrations)
            {
                string msdiLifetime = MapToServiceLifetime(reg.Lifetime);
                sb.AppendLine($"            services.Add(new global::Microsoft.Extensions.DependencyInjection.ServiceDescriptor(");
                sb.AppendLine($"                typeof(global::{reg.ServiceInterfaceFullyQualifiedName}),");
                sb.AppendLine($"                typeof(global::{reg.HandlerFullyQualifiedName}),");
