            string resultFQN = callSite.ResultFullyQualifiedName;

            sb.AppendLine("        internal static global::System.Threading.Tasks.ValueTask<TResult>");
            sb.AppendLine($"            {methodName}<TResult>(");
            sb.AppendLine("                this global::Nexum.Abstractions.ICommandDispatcher @this,");
            sb.AppendLine("                global::Nexum.Abstractions.ICommand<TResult> command,");
            sb.AppendLine("                global::System.Threading.CancellationToken ct = default)");
            sb.AppendLine("        {");
            sb.AppendLine($"            var __result = ((global::Nexum.IInterceptableDispatcher)@this)");
            sb.AppendLine($"                .DispatchInterceptedCommandAsync<global::{messageFQN}, global::{resultFQN}>(");
            sb.AppendLine($"                    (global::{messageFQN})(object)command,");
            sb.AppendLine("                    static (cmd, sp, ct) =>");
            sb.AppendLine("                    {");

            if (hasCompiledPipeline)
            {
                // Use Tier 2 compiled pipeline
                string pipelineMethodName = GeneratePipelineMethodName(handler!);
                sb.AppendLine($"                        return global::{rootNamespace}.NexumPipelineRegistry.{pipelineMethodName}(cmd, sp, ct);");
            }
            else
            {
                // Direct handler call (no Tier 2)
                // Handler is guaranteed non-null here (generator filters out call-sites without known handlers)
                string handlerFQN = handler!.HandlerFullyQualifiedName;
                sb.AppendLine("                        var handler = global::Microsoft.Extensions.DependencyInjection");
                sb.AppendLine("                            .ServiceProviderServiceExtensions");
                sb.AppendLine($"                            .GetRequiredService<global::{handlerFQN}>(sp);");
